// =============================================================================
// mask-code.js — превращает реальный промокод в маску для гостевого API.
//
// Гостям (неавторизованным пользователям лендинга) ПОЛНЫЙ код не отдаём —
// чтобы код реально нужно было заходить в ЛК после оформления подписки.
// Дефис сохраняется (он структурный для глаза), всё остальное → 'X'.
// =============================================================================
//
// Примеры:
//   maskCode('WB500RUB') === 'XXXXXXXX'
//   maskCode('ABC-1234') === 'XXX-XXXX'
//   maskCode('FOO-BAR-BAZ') === 'XXX-XXX-XXX'
//   maskCode('')       === ''
//   maskCode(null)     === ''
//   maskCode(undefined) === ''
// =============================================================================

export function maskCode(code) {
    if (code == null || code === '') return '';
    return String(code).replace(/[^-]/g, 'X');
}
