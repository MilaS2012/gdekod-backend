// =============================================================================
// id.js — единая точка генерации UUID.
//
// По умолчанию идентификаторы private_data таблиц генерируются на стороне
// БД через gen_random_uuid() (расширение pgcrypto). Если YC внезапно
// откажется давать право CREATE EXTENSION pgcrypto — переезжаем на
// генерацию в Node и убираем DEFAULT в DDL.
//
// Этот модуль — единственное место, где генерируется UUID на стороне
// приложения. Всегда импортируй newId() отсюда, не вызывай randomUUID()
// напрямую — чтобы при смене стратегии правки были в одном месте.
// =============================================================================

import { randomUUID } from 'node:crypto';

export const newId = () => randomUUID();
